// do while loop -- the code block will be executed at least once

let num=10;
do{
    console.log("Hello World");
}
while(num<5);

/*
we use do while -->  loop when we want to execute the code block at least once
for loop : when we know the number of iterations
while loop : when we don't know the number of iterations
*/