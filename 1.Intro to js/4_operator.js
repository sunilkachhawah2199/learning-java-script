/*  
arithmatic operator
+ --> add 
- --> subtraction
* --> multiply 
/ --> divide 
% --> remainder 
++ --> increment 
-- : decrement
** : exponentiation

assignment operator
= --> assignment
+= --> addition assignment --> a=ab+5 --> a+=5
-= --> subtraction assignment --> a=a-5 --> a-=5    
*= --> multiplication assignment --> a=a*5 --> a*=5
/= --> division assignment --> a=a/5 --> a/=5
%= --> remainder assignment --> a=a%5 --> a%=5

comparison operator
== --> equal value (compare only value)
=== --> equal value and equal type (compare value and type both) | return only true  if type and value both are same

> : greater than
< : less than
>= : greater than or equal to
<= : less than or equal to


!= --> not equal value
!== --> not equal value and not equal type



*/
// difference between == and ===
console.log(3 == "3");  // true
console.log(4 === "4");  // false

