// we use console.log() to print the output on the console.

// combine string and variable
let name="John";
let age=25;
console.log("Hello "+name+" your age is "+age); // same like java 
// or
console.log(`Hello ${name} your age is ${age}`); // template literals
// we can call function, variable literal also
// or 
console.log("Hello",name,"your age is",age); // comma operator
// or
console.log("Hello %s your age is %d",name,age); // format specifier